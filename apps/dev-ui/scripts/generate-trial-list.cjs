/**
 * Spec Template Auto-Discovery (Build-Time)
 *
 * Scans apps/simulation/specs/**\/*.toml and generates TypeScript spec list.
 * Runs before dev-ui build to ensure dropdown always shows latest specs.
 *
 * Output: src/generated/trial-templates.ts
 */

const fs = require('fs');
const path = require('path');

// Paths
const SPECS_DIR = path.join(__dirname, '../../simulation/specs');
const OUTPUT_FILE = path.join(__dirname, '../src/generated/trial-templates.ts');

/**
 * Parse TOML file to extract name and description from [meta] section
 * Simple regex-based parser (no external dependencies)
 */
function parseSpec(filePath, fileName, category) {
  const content = fs.readFileSync(filePath, 'utf-8');

  // Extract name from [meta] section: name = "..."
  const nameMatch = content.match(/^\s*name\s*=\s*"([^"]+)"/m);
  const name = nameMatch ? nameMatch[1] : fileName.replace('.toml', '');

  // Extract description field: description = """...""" or description = "..."
  let description = '';
  const multilineMatch = content.match(/^\s*description\s*=\s*"""\s*([^]*?)"""/m);
  const singleLineMatch = content.match(/^\s*description\s*=\s*"([^"]+)"/m);

  if (multilineMatch) {
    description = multilineMatch[1].trim().replace(/\n/g, ' ');
  } else if (singleLineMatch) {
    description = singleLineMatch[1];
  } else {
    description = 'No description provided';
  }

  // Truncate description to first sentence or 100 chars
  if (description.length > 100) {
    description = description.substring(0, 100).trim() + '...';
  }

  return {
    name: `${category}/${fileName.replace('.toml', '')}`,
    label: name,
    description,
    category,
  };
}

/**
 * Scan specs directory and its subfolders, generating TypeScript file
 */
function generateTrialList() {
  console.log('[generate-trial-list] Scanning specs directory...');

  // Verify specs directory exists
  if (!fs.existsSync(SPECS_DIR)) {
    console.error(`❌ Specs directory not found: ${SPECS_DIR}`);
    process.exit(1);
  }

  const specs = [];

  // Get subdirectories (behavior/, physics/, performance/)
  const categories = fs.readdirSync(SPECS_DIR)
    .filter(name => {
      const fullPath = path.join(SPECS_DIR, name);
      return fs.statSync(fullPath).isDirectory();
    })
    .sort();

  for (const category of categories) {
    const categoryDir = path.join(SPECS_DIR, category);
    console.log(`  [${category}]`);

    // Read all .toml files in this category
    const files = fs.readdirSync(categoryDir)
      .filter(file => file.endsWith('.toml'))
      .sort();

    for (const file of files) {
      const filePath = path.join(categoryDir, file);
      console.log(`    - ${file}`);
      specs.push(parseSpec(filePath, file, category));
    }
  }

  if (specs.length === 0) {
    console.warn('⚠️  No spec templates found in specs directory');
  }

  // Generate TypeScript code
  const tsCode = `// Auto-generated spec template list
// Generated by: scripts/generate-trial-list.cjs
// Source: apps/simulation/specs/ (all .toml files)
//
// DO NOT EDIT MANUALLY - Changes will be overwritten at build time

export interface TrialTemplate {
  name: string;
  label: string;
  description: string;
  category: string;
}

export const TRIAL_TEMPLATES: TrialTemplate[] = ${JSON.stringify(specs, null, 2)};
`;

  // Ensure output directory exists
  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  // Write generated file
  fs.writeFileSync(OUTPUT_FILE, tsCode, 'utf-8');

  console.log(`[generate-trial-list] ✅ Generated ${specs.length} spec templates`);
  console.log(`[generate-trial-list] Output: ${OUTPUT_FILE}`);
}

// Run if called directly
if (require.main === module) {
  generateTrialList();
}

module.exports = { generateTrialList };
