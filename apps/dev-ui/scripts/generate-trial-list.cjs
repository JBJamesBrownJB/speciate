/**
 * Trial Template Auto-Discovery (Build-Time)
 *
 * Scans apps/simulation/trials/*.toml and generates TypeScript trial list.
 * Runs before dev-ui build to ensure dropdown always shows latest trials.
 *
 * Output: src/generated/trial-templates.ts
 */

const fs = require('fs');
const path = require('path');

// Paths
const TRIALS_DIR = path.join(__dirname, '../../simulation/trials');
const OUTPUT_FILE = path.join(__dirname, '../src/generated/trial-templates.ts');

/**
 * Parse TOML file to extract name and description
 * Simple regex-based parser (no external dependencies)
 */
function parseTrial(filePath, fileName) {
  const content = fs.readFileSync(filePath, 'utf-8');

  // Extract name field: name = "..."
  const nameMatch = content.match(/^\s*name\s*=\s*"([^"]+)"/m);
  const name = nameMatch ? nameMatch[1] : fileName.replace('.toml', '');

  // Extract description field: description = """...""" or description = "..."
  let description = '';
  const multilineMatch = content.match(/^\s*description\s*=\s*"""\s*([^]*?)"""/m);
  const singleLineMatch = content.match(/^\s*description\s*=\s*"([^"]+)"/m);

  if (multilineMatch) {
    description = multilineMatch[1].trim().replace(/\n/g, ' ');
  } else if (singleLineMatch) {
    description = singleLineMatch[1];
  } else {
    description = 'No description provided';
  }

  // Truncate description to first sentence or 100 chars
  if (description.length > 100) {
    description = description.substring(0, 100).trim() + '...';
  }

  return {
    name: fileName.replace('.toml', ''),
    label: name,
    description,
  };
}

/**
 * Scan trials directory and generate TypeScript file
 */
function generateTrialList() {
  console.log('[generate-trial-list] Scanning trials directory...');

  // Verify trials directory exists
  if (!fs.existsSync(TRIALS_DIR)) {
    console.error(`❌ Trials directory not found: ${TRIALS_DIR}`);
    process.exit(1);
  }

  // Read all .toml files
  const files = fs.readdirSync(TRIALS_DIR)
    .filter(file => file.endsWith('.toml'))
    .sort();

  if (files.length === 0) {
    console.warn('⚠️  No trial templates found in trials directory');
  }

  // Parse each trial
  const trials = files.map(file => {
    const filePath = path.join(TRIALS_DIR, file);
    console.log(`  - ${file}`);
    return parseTrial(filePath, file);
  });

  // Generate TypeScript code
  const tsCode = `/**
 * Auto-generated trial template list
 * Generated by: scripts/generate-trial-list.cjs
 * Source: apps/simulation/trials/*.toml
 *
 * DO NOT EDIT MANUALLY - Changes will be overwritten at build time
 */

export interface TrialTemplate {
  name: string;
  label: string;
  description: string;
}

export const TRIAL_TEMPLATES: TrialTemplate[] = ${JSON.stringify(trials, null, 2)};
`;

  // Ensure output directory exists
  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  // Write generated file
  fs.writeFileSync(OUTPUT_FILE, tsCode, 'utf-8');

  console.log(`[generate-trial-list] ✅ Generated ${trials.length} trial templates`);
  console.log(`[generate-trial-list] Output: ${OUTPUT_FILE}`);
}

// Run if called directly
if (require.main === module) {
  generateTrialList();
}

module.exports = { generateTrialList };
